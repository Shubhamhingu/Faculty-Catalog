-- What % of Schools offer x Course
SELECT SU.SUBJECT,
ROUND((COUNT(SC.SCHOOL) * 100.0) /(SELECT COUNT(DISTINCT SCHOOL) FROM SCHOOL), 2) AS PERCENTAGE_OF_SCHOOLS_OFFERING_PARTICULAR_SUBJ
FROM
SCHOOL SC JOIN SUBJECT SU 
ON SC.PROFESSOR_KEY = SU.PROFESSOR_KEY
GROUP BY SU.SUBJECT
ORDER BY PERCENTAGE_OF_SCHOOLS_OFFERING_PARTICULAR_SUBJ DESC;